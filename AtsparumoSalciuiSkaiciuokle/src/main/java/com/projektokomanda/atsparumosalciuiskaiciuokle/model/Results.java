package com.projektokomanda.atsparumosalciuiskaiciuokle.model;

public class Results {
	
	private double s;
	private double v;
	private double we;
	private double wr;
	private double n;
	private double r;
	private double d;
	private double g1;
	private double gg1;
	private double gg2;
	private double fre1;
	private double fre2;
	private double ti;
	
	public Results() {
		
	}

	public Results(double s, double v, double we, double wr, double n, double r, double d, double g1, double gg1,
			double gg2, double fre1, double fre2, double ti) {
		this.s = s;
		this.v = v;
		this.we = we;
		this.wr = wr;
		this.n = n;
		this.r = r;
		this.d = d;
		this.g1 = g1;
		this.gg1 = gg1;
		this.gg2 = gg2;
		this.fre1 = fre1;
		this.fre2 = fre2;
		this.ti = ti;
	}

	public double getS() {
		return s;
	}

	public void setS(double s) {
		this.s = s;
	}

	public double getV() {
		return v;
	}

	public void setV(double v) {
		this.v = v;
	}

	public double getWe() {
		return we;
	}

	public void setWe(double we) {
		this.we = we;
	}

	public double getWr() {
		return wr;
	}

	public void setWr(double wr) {
		this.wr = wr;
	}

	public double getN() {
		return n;
	}

	public void setN(double n) {
		this.n = n;
	}

	public double getR() {
		return r;
	}

	public void setR(double r) {
		this.r = r;
	}

	public double getD() {
		return d;
	}

	public void setD(double d) {
		this.d = d;
	}

	public double getG1() {
		return g1;
	}

	public void setG1(double g1) {
		this.g1 = g1;
	}

	public double getGg1() {
		return gg1;
	}

	public void setGg1(double gg1) {
		this.gg1 = gg1;
	}

	public double getGg2() {
		return gg2;
	}

	public void setGg2(double gg2) {
		this.gg2 = gg2;
	}

	public double getFre1() {
		return fre1;
	}

	public void setFre1(double fre1) {
		this.fre1 = fre1;
	}

	public double getFre2() {
		return fre2;
	}

	public void setFre2(double fre2) {
		this.fre2 = fre2;
	}

	public double getTi() {
		return ti;
	}

	public void setTi(double ti) {
		this.ti = ti;
	}
	
}
